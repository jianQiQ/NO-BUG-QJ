//CAN接收中断回调函数，同时处理了编码器到距离的计算，更新到麦轮结构体中的current_pos.distance中
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
    CAN_RxHeaderTypeDef rx_header;
    uint8_t rx_data[8];
    can_rx_count++;
    HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &rx_header, rx_data);

    switch (rx_header.StdId)
    {
        case CAN_3508_M1_ID:
        case CAN_3508_M2_ID:
        case CAN_3508_M3_ID:
        case CAN_3508_M4_ID:
        case CAN_YAW_MOTOR_ID:
        case CAN_PIT_MOTOR_ID:
        case CAN_TRIGGER_MOTOR_ID:
        {
            static uint8_t i = 0;
            //get motor id
            i = rx_header.StdId - CAN_3508_M1_ID;
            get_motor_measure(&motor_chassis[i], rx_data); //数据处理，更新电机状态
            mecanum_ecd_distance(&mecanum); //编码器到距离的计算
            break;
        }

        default:
        {
            break;
        }
    }
}


//编码器到距离的计算，更新到麦轮结构体中的current_pos.distance中
void mecanum_ecd_distance(mecanum_control_t *mecanum_control)
{
    if (mecanum_control == NULL)
    {
        return;
    }
  float abs_wheel_distance_sum = 0.0f;
    for(int i=0; i<4; i++)//四个电机处理
    {
        const motor_measure_t *motor_data = get_chassis_motor_measure_point(i);
        if (motor_data == NULL) continue; //空指针处理

        uint16_t ecd = motor_data->ecd; //更新编码器值
        uint16_t last_ecd = mecanum_control->last_ecd[i]; //上次编码器值

        int32_t delta_ecd; //编码器变化量
         int32_t diff = (int32_t)ecd - (int32_t)last_ecd;
        
        // 处理C620编码器溢出 (0-8191)
        if (diff > 4096) {
            delta_ecd = diff - 8192;  // 反向溢出（电机反转时，编码器由小值变大值）
        } 
        else if (diff < -4096) {
            delta_ecd = diff + 8192;  // 正向溢出
        } 
        else {
            delta_ecd = diff;         // 正常情况
        }
        
        // 过滤异常大的跳变
        if (abs(delta_ecd) > MAX_ECD_DELTA) {
            continue; // 跳过异常数据
        }
        
        // 更新上次编码器值
        mecanum_control->last_ecd[i] = ecd;
        
        // 计算实际轮子移动距离 (米)
        // M2006距离计算: (编码器变化量 / 编码器分辨率) / 减速比 * 轮子周长
        float wheel_distance = ((float)delta_ecd / M2006_ENCODER_RES) / M2006_GEAR_RATIO * WHEEL_CIRCUMFERENCE;
        abs_wheel_distance_sum += fabsf(wheel_distance);

        mecanum_control->total_wheel_distance[i] += wheel_distance; //每个轮的累计距离

    }
    
    // 计算底盘平均移动距离
    float chassis_distance = abs_wheel_distance_sum / 4.0f; //简化公式
    
    mecanum_control->current_pos.distance += chassis_distance; //保存当前距离到麦轮结构体中
    
    float angle_rad = mecanum_control->current_pos.yaw * 3.1415f / 180.0f;  // 转换为弧度
    mecanum_control->current_pos.x += chassis_distance * cosf(angle_rad);  // X方向分量
    mecanum_control->current_pos.y += chassis_distance * sinf(angle_rad);  // Y方向分量
    
    // 如果有朝向角，分解到x/y轴
    /*
    float angle_rad = mecanum_control->current_pos.angle * M_PI / 180.0f;
    mecanum_control->current_pos.x += chassis_distance * cosf(angle_rad);
    mecanum_control->current_pos.y += chassis_distance * sinf(angle_rad);
    */
}



/**
  * @brief          send control current of motor (0x205, 0x206, 0x207, 0x208)
  * @param[in]      yaw: (0x205) 6020 motor control current, range [-30000,30000] 
  * @param[in]      pitch: (0x206) 6020 motor control current, range [-30000,30000]
  * @param[in]      shoot: (0x207) 2006 motor control current, range [-10000,10000]
  * @param[in]      rev: (0x208) reserve motor control current
  * @retval         none
  */
/**
  * @brief          发送电机控制电流(0x205,0x206,0x207,0x208)
  * @param[in]      yaw: (0x205) 6020电机控制电流, 范围 [-30000,30000]
  * @param[in]      pitch: (0x206) 6020电机控制电流, 范围 [-30000,30000]
  * @param[in]      shoot: (0x207) 2006电机控制电流, 范围 [-10000,10000]
  * @param[in]      rev: (0x208) 保留，电机控制电流
  * @retval         none
  */
void CAN_cmd_gimbal(int16_t yaw, int16_t pitch, int16_t shoot, int16_t rev)
{
    uint32_t send_mail_box;
    gimbal_tx_message.StdId = CAN_GIMBAL_ALL_ID;
    gimbal_tx_message.IDE = CAN_ID_STD;
    gimbal_tx_message.RTR = CAN_RTR_DATA;
    gimbal_tx_message.DLC = 0x08;
    gimbal_can_send_data[0] = (yaw >> 8);
    gimbal_can_send_data[1] = yaw;
    gimbal_can_send_data[2] = (pitch >> 8);
    gimbal_can_send_data[3] = pitch;
    gimbal_can_send_data[4] = (shoot >> 8);
    gimbal_can_send_data[5] = shoot;
    gimbal_can_send_data[6] = (rev >> 8);
    gimbal_can_send_data[7] = rev;
    HAL_CAN_AddTxMessage(&GIMBAL_CAN, &gimbal_tx_message, gimbal_can_send_data, &send_mail_box);
}

/**
  * @brief          send CAN packet of ID 0x700, it will set chassis motor 3508 to quick ID setting
  * @param[in]      none
  * @retval         none
  */
/**
  * @brief          发送ID为0x700的CAN包,它会设置3508电机进入快速设置ID
  * @param[in]      none
  * @retval         none
  */
void CAN_cmd_chassis_reset_ID(void)
{
    uint32_t send_mail_box;
    chassis_tx_message.StdId = 0x700;
    chassis_tx_message.IDE = CAN_ID_STD;
    chassis_tx_message.RTR = CAN_RTR_DATA;
    chassis_tx_message.DLC = 0x08;
    chassis_can_send_data[0] = 0;
    chassis_can_send_data[1] = 0;
    chassis_can_send_data[2] = 0;
    chassis_can_send_data[3] = 0;
    chassis_can_send_data[4] = 0;
    chassis_can_send_data[5] = 0;
    chassis_can_send_data[6] = 0;
    chassis_can_send_data[7] = 0;

    HAL_CAN_AddTxMessage(&CHASSIS_CAN, &chassis_tx_message, chassis_can_send_data, &send_mail_box);
}


/**
  * @brief          send control current of motor (0x201, 0x202, 0x203, 0x204)
  * @param[in]      motor1: (0x201) 3508 motor control current, range [-16384,16384] 
  * @param[in]      motor2: (0x202) 3508 motor control current, range [-16384,16384] 
  * @param[in]      motor3: (0x203) 3508 motor control current, range [-16384,16384] 
  * @param[in]      motor4: (0x204) 3508 motor control current, range [-16384,16384] 
  * @retval         none
  */
/**
  * @brief          发送电机控制电流(0x201,0x202,0x203,0x204)
  * @param[in]      motor1: (0x201) 3508电机控制电流, 范围 [-16384,16384]
  * @param[in]      motor2: (0x202) 3508电机控制电流, 范围 [-16384,16384]
  * @param[in]      motor3: (0x203) 3508电机控制电流, 范围 [-16384,16384]
  * @param[in]      motor4: (0x204) 3508电机控制电流, 范围 [-16384,16384]
  * @retval         none
  */
void CAN_cmd_chassis(int16_t motor1, int16_t motor2, int16_t motor3, int16_t motor4)
{
    uint32_t send_mail_box;
    chassis_tx_message.StdId = CAN_CHASSIS_ALL_ID;
    chassis_tx_message.IDE = CAN_ID_STD;
    chassis_tx_message.RTR = CAN_RTR_DATA;
    chassis_tx_message.DLC = 0x08;
    chassis_can_send_data[0] = motor1 >> 8;
    chassis_can_send_data[1] = motor1;
    chassis_can_send_data[2] = motor2 >> 8;
    chassis_can_send_data[3] = motor2;
    chassis_can_send_data[4] = motor3 >> 8;
    chassis_can_send_data[5] = motor3;
    chassis_can_send_data[6] = motor4 >> 8;
    chassis_can_send_data[7] = motor4;

    HAL_CAN_AddTxMessage(&CHASSIS_CAN, &chassis_tx_message, chassis_can_send_data, &send_mail_box);
}

